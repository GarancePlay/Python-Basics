"""
    Opperateurs en Pyhton :
        + addition
        - soustraction
        * multiplication
        / division
        % modulo (reste de la division euclidienne)
"""

calcul0 = (3 + 7) * 6
print(calcul0)

calcul1 = 5/2
calcul1 = int(calcul1)
print(calcul1)

calcul2 = 5/2
calcul2 = float(calcul2)
print(calcul2)

calcul3 = 5%2
# 5 = 2*2 + 1
print(calcul3)


